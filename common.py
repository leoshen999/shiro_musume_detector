
class Point:
    x = 0
    y = 0
    def __init__(self, x = 0, y = 0):
        self.x = x
        self.y = y

class Size:
    w = 0
    h = 0
    def __init__(self, w = 0, h = 0):
        self.w = w
        self.h = h

class Rect:
    x = 0
    y = 0
    w = 0
    h = 0
    def __init__(self, x = 0, y = 0, w = 0, h = 0):
        self.x = x
        self.y = y
        self.w = w
        self.h = h
